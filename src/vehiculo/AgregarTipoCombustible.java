    private void cargar(String valor){
            String col_name = " id_veh_combustible , descripcion";
            String tbl_name = " veh_combustible ";

            tbl_combustibe = Lib.limpiarTabla(tbl_combustibe);
            if(valor.trim().equals("")){

                tbl_combustibe.setModel(Lib.tblCargar((DefaultTableModel)tbl_combustibe.getModel(),Lib.queryArray(col_name, tbl_name," id_veh_combustible")));
              
            }else {
                
                
                tbl_combustibe.setModel(Lib.tblCargar((DefaultTableModel)tbl_combustibe.getModel(),Lib.queryArrayW(col_name, tbl_name," UPPER(descripcion) LIKE UPPER('"+ valor + "%') ORDER BY id_veh_combustible ")));
                

            }
    }

    private void agregarTipo(){

        if(Lib.validaString(txt_combustible.getText())){
            int ok = Lib.existeRegistro("*"," veh_combustible ","UPPER(descripcion) = UPPER('"+txt_combustible.getText()+"')");
            if( ok == 0){
                int id_ins_marca = Lib.queryInsert(new String[]{"descripcion"}, new String[]{txt_combustible.getText()}," veh_combustible ");
                if (id_ins_marca == 0){
                    JOptionPane.showMessageDialog(this,"No se Inserto ningun registro","Error no se guardaron datos",JOptionPane.ERROR_MESSAGE);
                    System.err.println("No se insertaron registro");

                }else{
                    cargar("");
                    limpiar();
                }
            }else{
                JOptionPane.showMessageDialog(this,"El tipo combustible se encuentra registrado","Dato duplicado",JOptionPane.ERROR_MESSAGE);
                System.err.println("El tipo de carroceria existe");
            }
        }else{
            
           JOptionPane.showMessageDialog(this,"Escriba el nombre de el tipo de combustible","Dato Invalido",JOptionPane.ERROR_MESSAGE);
           System.err.println("Dato invalido");
            
        }



    }
    
    private void actualizarTipo(){

        if(Lib.validaString(txt_combustible.getText()) && Lib.validaString(lbl_id.getText()) && lbl_id.getText() != "..."){

            if(Lib.existeRegistro("1"," veh_combustible ","UPPER(descripcion) = UPPER('"+txt_combustible.getText()+"') ") == 0){
                int ok = JOptionPane.showConfirmDialog(this, "Se actualizara el registro con el id: "+lbl_id.getText(),"Actualizar registro",JOptionPane.OK_OPTION, JOptionPane.WARNING_MESSAGE);
                if(ok == JOptionPane.YES_OPTION){
                    int id_ins_marca = Lib.queryUpdate(new String[]{"descripcion"}, new String[]{txt_combustible.getText()}," veh_combustible ","id_veh_combustible = '"+lbl_id.getText()+" '");
                    if (id_ins_marca == 0){
                        JOptionPane.showMessageDialog(this,"No se actualizo ningun registro","Error no se guardaron datos",JOptionPane.ERROR_MESSAGE);
                        System.err.println("No se actualizo registro");

                    }else{
                        cargar("");
                        limpiar();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this,"El tipo de combustible se encuentra registrado","Dato duplicado",JOptionPane.ERROR_MESSAGE);
                System.err.println("El tipo de carroceria existe");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Escriba el nombre de el tipo de combustible","Dato duplicado",JOptionPane.ERROR_MESSAGE);
            System.err.println("Dato invalido");
        }  
    }
    
    private void eliminarTipo(){

        if(Lib.validaString(lbl_id.getText()) && lbl_id.getText() != "..."){
            int ok = JOptionPane.showConfirmDialog(this, "Se eliminara el registro con el id: "+lbl_id.getText(),"Eliminar registro",JOptionPane.OK_OPTION, JOptionPane.WARNING_MESSAGE);
            if(ok == JOptionPane.YES_OPTION){
                int id_ins_marca = Lib.queryDelete(" veh_combustible ","id_veh_combustible",lbl_id.getText());
                if (id_ins_marca == 0){
                    JOptionPane.showMessageDialog(this,"No se elimino ningun registro","Error",JOptionPane.ERROR_MESSAGE);
                    System.err.println("No se elimino ningun registro");

                }else{
                    cargar("");
                    limpiar();
                }
            }
        }else{
            JOptionPane.showMessageDialog(this,"Selecione un registro  a borrar","Error",JOptionPane.ERROR_MESSAGE);
            System.err.println("No se a selecionado ningun registro");
        }        
 
    }
    
    private void limpiar(){
        
        lbl_id.setText("...");
        txt_combustible.setText("");
        txt_buscar.setText("");
        
    }

    private void selecionarDato(){
         
        int fila =  tbl_combustibe.getSelectedRow();
      
        if(fila ==-1) {
            JOptionPane.showMessageDialog(this,"Debes Seleccionar una Registro.");
        } else{    
      
       lbl_id.setText(tbl_combustibe.getValueAt(fila, 0).toString());
       txt_combustible.setText(tbl_combustibe.getValueAt(fila, 1).toString());
       
      }
        
    }


